export const getLegislations = (query?: string | null) => {
  if (!query) {
    return legislations;
  }
  const lowerCaseQuery = query.toLowerCase();
  return legislations.filter(
    (item) =>
      item.title.toLowerCase().includes(lowerCaseQuery) ||
      item.summary.toLowerCase().includes(lowerCaseQuery) ||
      item.category.toLowerCase().includes(lowerCaseQuery)
  );
};

export const getLegislationBySlug = (slug: string) => {
  return legislations.find((item) => item.slug === slug);
};

export const getLegislationsByCategory = (category: string) => {
  const decodedCategory = decodeURIComponent(category).replace(/-/g, ' ');
  return legislations.filter((item) => item.category.toLowerCase().replace(/-/g, ' ') === decodedCategory.toLowerCase());
};

export const getCategories = () => {
    const categories: { name: LegislationCategory, slug: string }[] = [...new Set(legislations.map(l => l.category))].map(name => ({
        name: name,
        slug: name.toLowerCase().replace(/\s+/g, '-')
    }));
    return categories;
}

export const getLegislationStats = () => {
  const stats: { [key in LegislationCategory]?: number } = {};
  
  legislations.forEach((item) => {
    if (stats[item.category] !== undefined) {
      (stats[item.category] as number) += 1;
    } else {
      stats[item.category] = 1;
    }
  });

  const allCategories = getCategories().map(c => c.name);
  allCategories.forEach(cat => {
    if(!stats[cat]) {
        stats[cat] = 0;
    }
  });

  return Object.entries(stats).map(([name, value]) => ({ name: name as LegislationCategory, value: value as number }));
};
